/////Calculos básicos//////////////////////////////

float Sumar(float num1, float num2) {       // SUMAR
    return num1 + num2;
}
float Restar(float num1, float num2) {      // RESTAR
    return num1 - num2;
}
float Multiplicar(float num1, float num2) { // MULTIPLICAR
    return num1 * num2;
}
float Dividir(float num1, float num2) {     // DIVIDIR
    return num1 / num2;
}

